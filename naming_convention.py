## Naming Conventions

# snake_case
    # Python standard naming
    # always lowercase
    # always underscored between words

# camelCase
    # JavaScript naming
    # first word, always lower case
    # Capitalize the first letter of each word that follows
    
# PascalCase
    # similar to camelCase but with every first letter of each word capitalized
    
# kebab-case
    # every-word-is-separated-by-a-dash

# ALL CAPS
    # represents a piece of data that will never change
NUM_PI = 3.14

